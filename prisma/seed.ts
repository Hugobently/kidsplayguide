// ============================================
// SEED FUNCTIONS
// ============================================

async function main() {
  console.log('üå± Starting database seed...\n');

  // Seed sources
  console.log('üì∫ Seeding game sources...');
  for (const source of SOURCES) {
    await prisma.gameSource.upsert({
      where: { slug: source.slug },
      update: source,
      create: source,
    });
  }
  console.log(`   ‚úì ${SOURCES.length} sources seeded\n`);

  // Seed categories
  console.log('üìÅ Seeding categories...');
  for (const category of CATEGORIES) {
    await prisma.category.upsert({
      where: { key: category.key },
      update: category,
      create: category,
    });
  }
  console.log(`   ‚úì ${CATEGORIES.length} categories seeded\n`);

  // Seed skills
  console.log('üéØ Seeding skills...');
  for (const skill of SKILLS) {
    await prisma.skill.upsert({
      where: { key: skill.key },
      update: skill,
      create: skill,
    });
  }
  console.log(`   ‚úì ${SKILLS.length} skills seeded\n`);

  // Seed themes
  console.log('üé® Seeding themes...');
  for (const theme of THEMES) {
    await prisma.theme.upsert({
      where: { key: theme.key },
      update: theme,
      create: theme,
    });
  }
  console.log(`   ‚úì ${THEMES.length} themes seeded\n`);

  // Seed games
  console.log('üéÆ Seeding games...');
  for (const gameData of GAMES) {
    const source = await prisma.gameSource.findUnique({
      where: { slug: gameData.sourceSlug },
    });

    if (!source) {
      console.log(`   ‚ö† Source not found: ${gameData.sourceSlug}`);
      continue;
    }

    const slug = generateSlug(gameData.title);
    const ageGroup = computeAgeGroup(gameData.minAge, gameData.maxAge);

    // Build game object
    const baseGame = {
      title: gameData.title,
      description: gameData.description,
      shortDescription: (gameData as any).shortDescription || null,
      whyWePicked: (gameData as any).whyWePicked || null,
      parentTip: (gameData as any).parentTip || null,
      parentInfo: (gameData as any).parentInfo || null,
      thumbnailUrl: (gameData as any).thumbnailUrl || null,
      gameUrl: gameData.gameUrl,
      sourceId: source.id,
      minAge: gameData.minAge,
      maxAge: gameData.maxAge,
      ageGroup,
      trustTier: source.trustTier,
      qualityConfidence: source.trustTier === 'TIER_1' ? 'HIGH' : 'MEDIUM',
      sessionLength: gameData.sessionLength || 'medium',
      pricingModel: gameData.pricingModel || 'free',
      // New editorial fields
      pros: JSON.stringify((gameData as any).pros || []),
      cons: JSON.stringify((gameData as any).cons || []),
      rating: (gameData as any).rating || 0,
      developerName: (gameData as any).developerName || null,
      featured: (gameData as any).featured || false,
      editorChoice: (gameData as any).editorChoice || false,
      // Gate criteria - all true for trusted sources
      hasNoInGameAds: true,
      hasRespectfulMonetization: true,
      hasNoContactWithStrangers: true,
      hasNoManipulativeDesign: true,
      isAgeAppropriate: true,
      // AI-assigned badges
      badgeEducational: gameData.badgeEducational || false,
      badgeCreative: gameData.badgeCreative || false,
      badgePopular: gameData.badgePopular || false,
      badgePolished: gameData.badgePolished || false,
    };

    // Compute gate criteria and badges
    const passesGateCriteria = computePassesGateCriteria(baseGame);
    const withBadges = assignBadges({ ...baseGame, passesGateCriteria });

    // Determine if game should be active
    const isActive = passesGateCriteria &&
      source.trustTier !== 'TIER_3' &&
      withBadges.qualityConfidence !== 'LOW';

    // Upsert game
    const game = await prisma.game.upsert({
      where: { slug },
      update: {
        ...withBadges,
        passesGateCriteria,
        isActive,
      },
      create: {
        slug,
        ...withBadges,
        passesGateCriteria,
        isActive,
      },
    });

    // Connect categories
    for (const categoryKey of gameData.categories) {
      const category = await prisma.category.findUnique({ where: { key: categoryKey } });
      if (category) {
        await prisma.gameCategory.upsert({
          where: { gameId_categoryId: { gameId: game.id, categoryId: category.id } },
          update: {},
          create: { gameId: game.id, categoryId: category.id },
        });
      }
    }

    // Connect skills
    for (const skillKey of gameData.skills) {
      const skill = await prisma.skill.findUnique({ where: { key: skillKey } });
      if (skill) {
        await prisma.gameSkill.upsert({
          where: { gameId_skillId: { gameId: game.id, skillId: skill.id } },
          update: {},
          create: { gameId: game.id, skillId: skill.id },
        });
      }
    }

    // Connect themes
    for (const themeKey of gameData.themes) {
      const theme = await prisma.theme.findUnique({ where: { key: themeKey } });
      if (theme) {
        await prisma.gameTheme.upsert({
          where: { gameId_themeId: { gameId: game.id, themeId: theme.id } },
          update: {},
          create: { gameId: game.id, themeId: theme.id },
        });
      }
    }

    console.log(`   ‚úì ${gameData.title} (${isActive ? 'active' : 'inactive'})`);
  }
  console.log(`\n   ‚úì ${GAMES.length} games seeded\n`);

  console.log('‚úÖ Database seed complete!');
}

main()
  .catch((e) => {
    console.error('‚ùå Seed failed:', e);
    process.exit(1);
  })
  .finally(async () => {
    await prisma.$disconnect();
  });
